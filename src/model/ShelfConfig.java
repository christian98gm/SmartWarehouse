package model;

public class ShelfConfig {

    private int id;
    private int length;

    public ShelfConfig() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

}